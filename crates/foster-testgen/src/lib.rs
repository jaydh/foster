    fn sdk_pascal_case_multi_word() {
        let m = MachineBuilder::new("my_machine", "idle", json!({}))
            .state("busy")
            .pass("idle", "start", "busy")
            .pass("busy", "stop", "idle")
            .build();
        let out = generate_sdk(&m, "http://localhost:9000");
        assert!(out.contains("export class MyMachineClient"),    "PascalCase class name");
        assert!(out.contains("export type MyMachineState"),      "PascalCase state type");
        assert!(out.contains("export type MyMachineEvent"),      "PascalCase event type");
        assert!(out.contains("export interface MyMachineSnapshot"), "PascalCase snapshot type");
    }

