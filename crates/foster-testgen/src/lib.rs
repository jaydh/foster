        assert_eq!(to_pascal_case("counter"),      "Counter");
        assert_eq!(to_pascal_case("my_machine"),   "MyMachine");
        assert_eq!(to_pascal_case("my-machine"),   "MyMachine");
        assert_eq!(to_pascal_case("foo_bar_baz"),  "FooBarBaz");
